package com.nearsoft.apprentice.dip.bad;

public class Player {

    private int number;
    private String name;
    private Barcelona team;

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Barcelona getTeam() {
        return team;
    }

    public void setTeam(Barcelona team) {
        this.team = team;
    }

}
